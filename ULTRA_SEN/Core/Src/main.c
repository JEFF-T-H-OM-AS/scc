// Microsecond delay function using TIM2
void delay_us(uint16_t us) {
    __HAL_TIM_SET_COUNTER(&htim2, 0);
    while (__HAL_TIM_GET_COUNTER(&htim2) < us);
}

// Send 10Âµs trigger pulse
void trigger_ultrasonic() {
    HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_SET);
    delay_us(10);
    HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_RESET);
}

// Measure echo pulse width in microseconds
uint32_t measure_echo_time() {
    uint32_t start = 0, stop = 0;

    while (HAL_GPIO_ReadPin(ECHO_PORT, ECHO_PIN) == GPIO_PIN_RESET);
    start = __HAL_TIM_GET_COUNTER(&htim2);

    while (HAL_GPIO_ReadPin(ECHO_PORT, ECHO_PIN) == GPIO_PIN_SET);
    stop = __HAL_TIM_GET_COUNTER(&htim2);

    return (stop - start);
}

// Convert echo time to distance in cm
float calculate_distance(uint32_t time_us) {
    return (time_us * 0.0343) / 2;
}
